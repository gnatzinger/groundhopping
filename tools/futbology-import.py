def slug(s):
    s = s.replace("ä","ae").replace("ö","oe").replace("ü","ue").replace("ß","ss")
    s = unicodedata.normalize("NFKD", s).encode("ascii","ignore").decode()
    return re.sub(r"-+","-",re.sub(r"[^a-z0-9]+","-",s.lower())).strip("-")

def q(s):
    return '"' + s.replace('"','\\"') + '"'

# Das früheste Spiel je Stadion trägt die Stadion-Daten
earliest = {}
for m in M:
    gid = m[1]
    if gid not in earliest or m[0] < earliest[gid]:
        earliest[gid] = m[0]

